    // Metode Math
console.log(Math.abs(-7)) // maka hasilnya 7,dan Absolute

console.log(Math.pow(3,4)) //maka outputnya 81, 3 pangkat 4
        //method 'pow' ini digunakan untuk 'Perpangkatan'
        //kiri adalah angka object,yang kanan nilai angka yg di gunakan untuk di pangkat kan

console.log(Math.sqrt(36)) //maka outputnya 6, akar dari bilangan 36 adalah 6 (6x6=36)
        //method 'sqrt' ini di gunakan untuk mencari 'akar' dari suatu nilai (perakaran)

console.log(Math.cbrt(27)) //maka outputnya 3, karena 3 x 3 x 3 hasilnya 27
        //method 'cbrt' ini digunakan untuk mencari nilai awal dari Pangkat 3 (2x2x2) 
        //cbrt di gunakanuntuk mencari nilai awal,sebelum hasil yang kita tulis (contoh 27 yang hasil dari 3x3x3)
        
console.log(Math.max(1,13,82,2,75)) // maka outputnya 82,angka paling maksimum(besar adalah 82)
        //method 'max' ini di gunakan untuk mencar nilai paling max(banyak/besar)