    //Operator Aritmatike

// Penjumlahan (+)
let x = 5
let y = 10
let sum = x + y // maka output nya 15

console.log(sum, typeof sum)


// Pengurangan (-)
let a = 7 
let b = 3
let dif = a - b // maka output nya 4

console.log(dif, typeof dif)


// Perkalian (*)
let p = 5
let q = 4
let non = p * q // maka output nya 20

console.log(non,typeof non)


// Pembagian (/)
let m = 160
let n = 4
let gif = m / n // maka output nya 40

console.log(gif,typeof gif)


// Modulus (%) operasi sisa bagi
let r = 15
let s = 4
let mod = r % s //angka 15 aakan di bagi sampai tidak bisa di kurangi 
                //Output nya adalah 3(sisa dari pembagian adalah 3)

console.log(mod,typeof mod)


// Eksponen (**) Perpangkatan
let base = 2
let eksponen = 3
let power = base ** eksponen // maka output nya 8, Karena 2 pangkat 3 adalah 8

console.log(power,typeof power)


// Increment (++) menambah satu nilai
let count = 5
// count = count + 1 
//sama seperti count++ (menambahkan suatu nilai dalam variabe)
count++    //Maka output nya adlah 6
                  //Nilai yang di tambah satu nilai

console.log(count,typeof count)

// Decrement (--) mengurangi satu nilai
let minus = 5
// let minus = minus - 1
// sama seperti minus-- (menggurangi suatu nilai dalam variable)
minus--     //Maka output nya adalah 4
                //Nilai yang di kurangi satu nilai

console.log(minus,typeof minus)