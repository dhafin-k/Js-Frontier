
    // Operator logika

// And && operator
let a = true
let b = true
let hasil = a && b
    //butuh dua nilai yang sama untuk output true
console.log(hasil) //maka output nya true
                //hasilnya akan true jika dua nilai nya sama(true)
                //jika satu saja beda maka hasil akan false

// Or || operator
let c = true
let d = false
let will = c || d
    //butuh satu nilai true untuk output true
console.log(will) // maka outputnya akan true 
                //or akan mengembaikan true walau satu nilai nya berbedah
                //selagi ada nilai true dan ada false maka hasil nya akan true

// Not ! operator
let e = true
let notresult = !e
    //karena not ! akan membalikan nilai tersebut 
console.log(notresult) //maka output nya false
                //jika ia true maka hasil false,jika ia false maka hasil true